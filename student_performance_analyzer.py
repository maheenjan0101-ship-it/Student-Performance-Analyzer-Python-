def highest_scorer():
    """Finds and displays the student with the highest total marks."""

    if len(students) == 0:
        print("No students found.")
        return

    topper = students[0]

    for student in students:
        total_student = (
            student["math"] +
            student["english"] +
            student["science"]
            )
        total_topper = (
            topper["math"] +
            topper["english"] +
            topper["science"]
            )
        if total_student > total_topper:
          topper = student
    print("\n===== Highest Scorer =====")
    print("Roll No:", topper["roll_no"])
    print("Name:", topper["name"])
    print("Math:", topper["math"])
    print("English:", topper["english"])
    print("Science:", topper["science"])

    average = calculate_average(topper)
    print("Average:", round(average, 2))
    grade = calculate_grade(average)
    print("Grade:", grade)

def pass_percentage():
    """Calculates the percentage of students who passed."""

    if len(students) == 0:
        print("No students found.")
        return
    passed = 0
    for student in students:
        average = calculate_average(student)
        grade = calculate_grade(average)
        if grade != "F":
            passed += 1
    percentage = (passed/len(students))*100

    print("\n===== Percentage of Students Passed =====")
    print("Total Students:", len(students))
    print("Students passed:", passed)
    print("Failed students:", len(students)-passed)
    print("Pass Percentage:", round(percentage, 2), "%")

def sort_students():
    """Sorts and displays students by average marks in descending order."""

    if len(students) == 0:
        print("No students found.")
        return
    sorted_students = sorted(
        students,
        key = lambda student: calculate_average(student),
        reverse = True
    )
    print("\n===== Sorted Students =====")

    rank = 1

    for student in sorted_students:
        average = calculate_average(student)
        grade = calculate_grade(average)

        print(f"{rank}. {student['name']} (Roll No: {student['roll_no']})")
        print("Math:", student["math"])
        print("English:", student["english"])
        print("Science:", student["science"])
        print("Average:", round(average, 2))
        print("Grade:", grade)
        print()

        rank += 1

